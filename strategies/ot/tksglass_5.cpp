// sphere:stateless
/**
 * tksglass_5.cpp — Faithful C++ port of the tksglass OT Visualizer.
 *
 * Source: https://tksglass.github.io/OT/
 * Created by @tksglass
 *
 * Algorithm:
 *   1. Enumerate all valid board placements (exact DFS, mirroring
 *      runExactSolver() in the JS source).
 *   2. Compute p[i] = fraction of valid boards with a ship at cell i.
 *   3. Pick cell using mode-dependent scoring:
 *      - Explore mode (ships_hit < THRESHOLD or blues_used < 3):
 *          score = entropy*0.7 + |p-0.5|*0.6 + max(p,1-p)*0.3
 *          Pick highest score.  Guaranteed move: p[i] == 0 (certain blue).
 *      - Hunt mode (ships_hit >= THRESHOLD AND blues_used >= 3):
 *          Pick highest p[i] (most likely non-blue).
 *          Guaranteed move: p[i] == 1 (certain ship).
 *   4. Opening move (nothing clicked yet): cell (0,0) — top-left corner.
 *
 * THRESHOLD = 5 (default / "Extra Chance Risk Level 5" in the tool).
 * For threshold 4, see tksglass_4.cpp.
 *
 * Faithfulness notes:
 *   - Ships: teal(4), green(3), yellow(3), plus n_rare rare ships of size 2.
 *     n_rare = n_colors - 4 (so n_colors 6→2 rares, ..., 9→5 rares).
 *   - Identified rare cells (spO/spL/spD/spR/spW visible in the grid) are
 *     treated as named rares with placement constraints.  Unidentified rare
 *     slots are anonymous (rare_0, rare_1, ...).
 *   - Ships are sorted by fewest valid placements ascending before DFS
 *     (mirrors the JS sort inside runExactSolver).
 *   - The JS DFS does NOT apply a canonical ordering guard for rare ships;
 *     we replicate this exactly.  Since each ship occupies a unique slot id
 *     (its post-sort index), no two boards generated by the DFS are identical,
 *     so no deduplication set is needed (unlike in the JS, where rare_0/rare_1
 *     could theoretically swap — but the occupiedMask check prevents any real
 *     duplicate from being produced).
 *   - The diagonal-neighbor same-color constraint in the JS is vacuously
 *     satisfied in OT (each color appears exactly once) and is not checked.
 *   - isBoardValid() at the leaf is not separately called; placement generation
 *     already guarantees consistency with the revealed grid, and our placements
 *     contain exactly `size` cells each.
 *   - Scoring formula is exactly the JS formula.
 */

