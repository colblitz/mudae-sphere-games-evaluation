// ---------------------------------------------------------------------------
// Threshold — hit count at which hunt mode activates (alongside blues_used>=3)
// ---------------------------------------------------------------------------

static constexpr int THRESHOLD = 5;

// ---------------------------------------------------------------------------
// Grid size
// ---------------------------------------------------------------------------

static constexpr int GRID = 25;

// ---------------------------------------------------------------------------
// Cell color enum
// ---------------------------------------------------------------------------

enum CellColor : uint8_t {
    CC_TEAL    = 0,
    CC_GREEN   = 1,
    CC_YELLOW  = 2,
    CC_ORANGE  = 3,
    CC_LIGHT   = 4,
    CC_DARK    = 5,
    CC_RED     = 6,
    CC_WHITE   = 7,   // rainbow = spW
    CC_BLUE    = 8,
    CC_UNKNOWN = 15,
};

static inline CellColor parseColor(const char* sp) {
    // sp is e.g. "spT", "spG", ... — dispatch on sp[2]
    switch (sp[2]) {
        case 'T': return CC_TEAL;
        case 'G': return CC_GREEN;
        case 'Y': return CC_YELLOW;
        case 'O': return CC_ORANGE;
        case 'L': return CC_LIGHT;
        case 'D': return CC_DARK;
        case 'R': return CC_RED;
        case 'W': return CC_WHITE;
        case 'B': return CC_BLUE;
        default:  return CC_UNKNOWN;
    }
}

static inline bool isRareColor(CellColor c) {
    return c >= CC_ORANGE && c <= CC_WHITE;
}

// ---------------------------------------------------------------------------
// Revealed state — clicked mask + per-cell color
// ---------------------------------------------------------------------------

struct RevealedState {
    uint32_t  clickedMask;   // bit i = cell i has been clicked
    CellColor col[GRID];     // CC_UNKNOWN if not clicked
};

// ---------------------------------------------------------------------------
// Placement bitmask tables — precomputed for sizes 2, 3, 4.
//
// JS iteration order (from getValidPlacements):
//   for r in 0..4:
//     for c in 0..4:
//       for horiz in [true, false]:   ← true first
//         generate placement starting at (r,c) in that direction
//
// We replicate this order exactly.
// ---------------------------------------------------------------------------

struct PlacementList {
    uint32_t masks[40];  // max 40 entries for size-2
    int      n;
};

static PlacementList buildPlacementList(int size) {
    PlacementList pl; pl.n = 0;
    for (int r = 0; r < 5; ++r) {
        for (int c = 0; c < 5; ++c) {
            for (int horiz = 1; horiz >= 0; --horiz) {  // true (1) first, then false (0)
                uint32_t m = 0;
                bool valid = true;
                for (int k = 0; k < size; ++k) {
                    int rr = r + (horiz ? 0 : k);
                    int cc = c + (horiz ? k : 0);
                    if (rr >= 5 || cc >= 5) { valid = false; break; }
                    m |= 1u << (rr * 5 + cc);
                }
                if (!valid || __builtin_popcount(m) != size) continue;
                pl.masks[pl.n++] = m;
            }
        }
    }
    return pl;
}

static const PlacementList& allPlacements(int size) {
    static PlacementList p2 = buildPlacementList(2);
    static PlacementList p3 = buildPlacementList(3);
    static PlacementList p4 = buildPlacementList(4);
    if (size == 2) return p2;
    if (size == 3) return p3;
    return p4;
}

// ---------------------------------------------------------------------------
// Direction constraint — mirrors JS getDirectionConstraint(cells).
// 0=any, 1=horizontal, 2=vertical.
// Input: bitmask of already-revealed cells for this ship color.
// ---------------------------------------------------------------------------

static int directionConstraint(uint32_t revealMask) {
    if (__builtin_popcount(revealMask) < 2) return 0;  // any
    int firstRow = __builtin_ctz(revealMask) / 5;
    bool sameRow = true;
    for (uint32_t m = revealMask; m; m &= m - 1)
        if (__builtin_ctz(m) / 5 != firstRow) { sameRow = false; break; }
    if (sameRow) return 1;
    int firstCol = __builtin_ctz(revealMask) % 5;
    bool sameCol = true;
    for (uint32_t m = revealMask; m; m &= m - 1)
        if (__builtin_ctz(m) % 5 != firstCol) { sameCol = false; break; }
    if (sameCol) return 2;
    return 0;
}

// ---------------------------------------------------------------------------
// Ship descriptor
// ---------------------------------------------------------------------------

struct ShipSlot {
    int      size;
    uint32_t revealMask;    // cells already revealed as this ship's color
    int      dirConstraint; // 0=any, 1=horiz, 2=vert
    bool     isAnon;        // true if anonymous rare (no revealed cells by identity)
};

// ---------------------------------------------------------------------------
// Valid placements for one ship slot, given the current revealed state.
//
// Mirrors JS getValidPlacements(color):
//
//   For named ships (teal/green/yellow/identified-rare):
//     - Must cover all cells in revealMask (known cells of this color).
//     - Must not cover any cell revealed as a DIFFERENT non-blue color.
//     - Must not cover any blue cell (isBoardValid enforces this; we include
//       it here so we never propose an invalid placement).
//     - Direction constraint applied if >=2 known cells exist.
//
//   For anonymous rares (rare_k):
//     - revealMask = 0 (no known cells).
//     - Must not cover any cell revealed as ANY non-blue color.
//     - Must not cover any blue cell.
//
//   JS quote:
//     if(grid[i] && grid[i] !== 'miss'){          // non-blue revealed cell
//       if(color.startsWith('rare_')){ valid=false; }  // anon: any ship cell conflicts
//       else { if(grid[i] !== color) valid=false; }    // named: only other colors conflict
//     }
//   Note: blue ('miss') does NOT conflict in getValidPlacements for either type.
//   However isBoardValid(board,true) checks grid[i]==='miss' → board[i] null,
//   so blue cells must also be excluded (we add that constraint here).
// ---------------------------------------------------------------------------

static std::vector<uint32_t> validPlacements(
    const ShipSlot& slot,
    const RevealedState& rev)
{
    // selfMask: cells that must be included (revealed as this ship's color).
    uint32_t selfMask = slot.revealMask;

    // conflictMask: cells that block this ship.
    // For named ships: other ships' cells + blue cells.
    // For anon rares:  any ship cell + blue cells.
    // In both cases: all clicked cells that are not in selfMask.
    //
    // Equivalently: all clicked cells whose color is not this ship's own color.
    // Since anon rares have revealMask=0, all clicked cells conflict for them.
    uint32_t conflictMask = 0;
    for (int i = 0; i < GRID; ++i) {
        if (!(rev.clickedMask & (1u << i))) continue;
        CellColor c = rev.col[i];
        if (c == CC_UNKNOWN) continue;
        // For blue cells: always conflict (ship can't go on a blue cell).
        // For non-blue cells: conflict unless it's our own ship's cell.
        if (c == CC_BLUE || !(selfMask & (1u << i))) {
            conflictMask |= 1u << i;
        }
    }

    const PlacementList& pl = allPlacements(slot.size);
    std::vector<uint32_t> result;
    result.reserve(pl.n);

    for (int pi = 0; pi < pl.n; ++pi) {
        uint32_t m = pl.masks[pi];

        // Apply direction constraint
        if (slot.dirConstraint == 1) {
            // Horizontal: all cells in same row
            int r0 = __builtin_ctz(m) / 5;
            bool ok = true;
            for (uint32_t tmp = m; tmp; tmp &= tmp - 1)
                if (__builtin_ctz(tmp) / 5 != r0) { ok = false; break; }
            if (!ok) continue;
        } else if (slot.dirConstraint == 2) {
            // Vertical: all cells in same column
            int c0 = __builtin_ctz(m) % 5;
            bool ok = true;
            for (uint32_t tmp = m; tmp; tmp &= tmp - 1)
                if (__builtin_ctz(tmp) % 5 != c0) { ok = false; break; }
            if (!ok) continue;
        }

        // Must include all self cells
        if ((m & selfMask) != selfMask) continue;

        // Must not overlap any conflict cell
        if (m & conflictMask) continue;

        result.push_back(m);
    }

    return result;
}

// ---------------------------------------------------------------------------
// Build the ship list for a given board state.
//
// Mirrors JS:
//   getShipList() = ['teal','green','yellow', ...identifiedRares, 'rare_0',...]
//   runExactSolver sorts by getPlacementsCached(s).length ascending.
// ---------------------------------------------------------------------------

struct ShipInfo {
    ShipSlot              slot;
    std::vector<uint32_t> placements;
};

static std::vector<ShipInfo> buildShipList(
    const RevealedState& rev,
    int nColors)
{
    int nRare = nColors - 4;

    // Collect identified rare colors (in order of first revealed cell, LSB first).
    CellColor knownRareCol[5];
    uint32_t  knownRareMask[5];
    int       nKnownRare = 0;
    for (int i = 0; i < GRID; ++i) {
        CellColor c = rev.col[i];
        if (!isRareColor(c)) continue;
        bool found = false;
        for (int k = 0; k < nKnownRare; ++k)
            if (knownRareCol[k] == c) { knownRareMask[k] |= 1u << i; found = true; break; }
        if (!found && nKnownRare < 5) {
            knownRareCol[nKnownRare]  = c;
            knownRareMask[nKnownRare] = 1u << i;
            ++nKnownRare;
        }
    }
    int nAnonRare = std::max(0, nRare - nKnownRare);

    std::vector<ShipInfo> ships;
    ships.reserve(3 + nKnownRare + nAnonRare);

    // Helper lambda to add one ship
    auto addShip = [&](int size, uint32_t revMask, bool anon) {
        ShipSlot s;
        s.size          = size;
        s.revealMask    = revMask;
        s.dirConstraint = directionConstraint(revMask);
        s.isAnon        = anon;
        ShipInfo si;
        si.slot       = s;
        si.placements = validPlacements(s, rev);
        ships.push_back(std::move(si));
    };

    // Named fixed ships
    {
        uint32_t m = 0;
        for (int i = 0; i < GRID; ++i) if (rev.col[i] == CC_TEAL)   m |= 1u << i;
        addShip(4, m, false);
    }
    {
        uint32_t m = 0;
        for (int i = 0; i < GRID; ++i) if (rev.col[i] == CC_GREEN)  m |= 1u << i;
        addShip(3, m, false);
    }
    {
        uint32_t m = 0;
        for (int i = 0; i < GRID; ++i) if (rev.col[i] == CC_YELLOW) m |= 1u << i;
        addShip(3, m, false);
    }
    // Identified rares (in first-reveal order, mirrors JS getUsedRare())
    for (int k = 0; k < nKnownRare; ++k)
        addShip(2, knownRareMask[k], false);
    // Anonymous rare slots
    for (int k = 0; k < nAnonRare; ++k)
        addShip(2, 0, true);

    // Sort by valid-placement count ascending (mirrors JS runExactSolver sort)
    std::stable_sort(ships.begin(), ships.end(), [](const ShipInfo& a, const ShipInfo& b) {
        return a.placements.size() < b.placements.size();
    });

    return ships;
}

// ---------------------------------------------------------------------------
// DFS board enumeration — mirrors runExactSolver's dfs() in JS.
//
// JS dfs at each level:
//   for opt in placementsMap[ships[index]]:
//     if any cell in opt is occupied (board[i] truthy): skip
//     place, recurse, unplace
//
// At the leaf: isBoardValid(board, true) is called.  We skip this call
// because our placement generation already guarantees:
//   - All ship cells are consistent with the revealed grid.
//   - No two ships share a cell (occupiedMask check).
//   - Each placement has exactly `size` cells.
// The only thing isBoardValid additionally checks that we can't trivially
// guarantee is that revealed blue cells have no ship on them — but we exclude
// blue cells from valid placements via conflictMask, so this is also satisfied.
//
// Deduplication: JS uses a seen-Set on board.join(',').  Since each ship slot
// gets a unique uint8_t id (its 1-based index in the sorted list) and the
// occupiedMask check prevents two ships from sharing a cell, the DFS can never
// produce two identical board encodings.  No deduplication set is needed.
// ---------------------------------------------------------------------------

struct DFSContext {
    const std::vector<ShipInfo>* ships;
    int                          nShips;
    uint8_t                      board[GRID];  // 0=empty, 1..nShips = ship slot id
    uint32_t                     occupiedMask;
    uint32_t                     counts[GRID]; // ship-hit count per cell
    uint32_t                     total;        // valid board count

    void run(int idx) {
        if (idx == nShips) {
            ++total;
            for (int i = 0; i < GRID; ++i)
                if (board[i]) counts[i]++;
            return;
        }

        const std::vector<uint32_t>& pl = (*ships)[idx].placements;
        const uint8_t slotId = (uint8_t)(idx + 1);

        for (uint32_t m : pl) {
            // JS: if(opt.some(i => board[i] && board[i] !== ship)) continue;
            // Since we don't pre-place anything, board[i] is only set by
            // previously-placed ships.  board[i] !== ship is always true here
            // (each ship has a unique slot id and fills different cells).
            // So the condition reduces to: any cell in opt already occupied.
            if (m & occupiedMask) continue;

            // Place
            occupiedMask |= m;
            for (uint32_t tmp = m; tmp; tmp &= tmp - 1)
                board[__builtin_ctz(tmp)] = slotId;

            run(idx + 1);

            // Unplace
            occupiedMask &= ~m;
            for (uint32_t tmp = m; tmp; tmp &= tmp - 1)
                board[__builtin_ctz(tmp)] = 0;
        }
    }
};

// ---------------------------------------------------------------------------
// Compute per-cell ship-occupancy probabilities.
// p[i] = fraction of valid boards where cell i is occupied by any ship.
// Returns false if no valid boards exist.
// ---------------------------------------------------------------------------

static bool computeProbs(
    const RevealedState& rev,
    int nColors,
    double probs[GRID])
{
    std::vector<ShipInfo> ships = buildShipList(rev, nColors);
    int nShips = (int)ships.size();

    // Early exit: if any ship has no valid placements, no valid board exists.
    for (int i = 0; i < nShips; ++i)
        if (ships[i].placements.empty()) return false;

    DFSContext ctx;
    ctx.ships        = &ships;
    ctx.nShips       = nShips;
    ctx.occupiedMask = 0;
    ctx.total        = 0;
    memset(ctx.board,  0, sizeof(ctx.board));
    memset(ctx.counts, 0, sizeof(ctx.counts));

    ctx.run(0);

    if (ctx.total == 0) return false;

    double inv = 1.0 / (double)ctx.total;
    for (int i = 0; i < GRID; ++i)
        probs[i] = (double)ctx.counts[i] * inv;

    return true;
}

// ---------------------------------------------------------------------------
// Score and pick best cell — mirrors highlightRecommendations() in JS.
//
// huntMode = (ships_hit >= THRESHOLD) && (blues_used >= 3)
//
// Hunt mode:
//   - Guaranteed: pick any cell with p == 1 (certain ship).
//   - Otherwise: pick highest p (most likely non-blue).
//
// Explore mode:
//   - Guaranteed: pick any cell with p == 0 (certain blue).
//   - Otherwise: score = entropy*0.7 + |p-0.5|*0.6 + max(p,1-p)*0.3
//     Pick highest score.
//
// JS scans candidates in array order (index 0..24, excluding revealed cells).
// Ties in guaranteed-move scan break on lowest cell index (first found).
// Ties in the scoring comparisons break on lowest cell index (first found
// wins in JS's sort since it's stable and candidates are ordered 0..24).
// ---------------------------------------------------------------------------

static int scoreAndPick(
    const double probs[GRID],
    const RevealedState& rev,
    int shipsHit,
    int bluesUsed)
{
    bool huntMode = (shipsHit >= THRESHOLD) && (bluesUsed >= 3);

    // Guaranteed move check (mirrors JS candidates.find(...))
    // JS iterates candidates in index order (probs.map(...).filter(!grid[i]))
    // which preserves ascending cell index.
    for (int i = 0; i < GRID; ++i) {
        if ((rev.clickedMask >> i) & 1) continue;
        if (huntMode  && probs[i] == 1.0) return i;
        if (!huntMode && probs[i] == 0.0) return i;
    }

    if (huntMode) {
        // candidates.sort((a,b) => b.p - a.p) → pick [0] = highest p.
        // Ties: JS stable sort preserves index order, so lowest index wins.
        int    best  = -1;
        double bestP = -1.0;
        for (int i = 0; i < GRID; ++i) {
            if ((rev.clickedMask >> i) & 1) continue;
            if (probs[i] > bestP) { bestP = probs[i]; best = i; }
        }
        return best;
    } else {
        // Explore mode: score formula.
        // Ties: JS stable sort preserves index order → lowest index wins.
        int    best      = -1;
        double bestScore = -std::numeric_limits<double>::infinity();
        for (int i = 0; i < GRID; ++i) {
            if ((rev.clickedMask >> i) & 1) continue;
            double p = probs[i];
            double entropy = (p == 0.0 || p == 1.0)
                ? 0.0
                : -p * std::log2(p) - (1.0 - p) * std::log2(1.0 - p);
            double asymmetry     = (p >= 0.5) ? (p - 0.5) : (0.5 - p);
            double collapseBonus = (p >= 0.5) ? p : (1.0 - p);
            double score = entropy * 0.7 + asymmetry * 0.6 + collapseBonus * 0.3;
            if (score > bestScore) { bestScore = score; best = i; }
        }
        return best;
    }
}

// ---------------------------------------------------------------------------
// Strategy class
// ---------------------------------------------------------------------------

class TKSGlass5OTStrategy : public OTStrategy {
public:
    void next_click(const std::vector<Cell>& board,
                    const std::string& meta_json,
                    ClickResult& out) override
    {
        // Parse meta
        const char* mj = meta_json.c_str();
        int nColors = 6, shipsHit = 0, bluesUsed = 0;
        const char* p;
        if ((p = strstr(mj, "\"n_colors\":")))   { p += 11; while (*p == ' ') ++p; nColors   = atoi(p); }
        if ((p = strstr(mj, "\"ships_hit\":")))  { p += 12; while (*p == ' ') ++p; shipsHit  = atoi(p); }
        if ((p = strstr(mj, "\"blues_used\":"))) { p += 13; while (*p == ' ') ++p; bluesUsed = atoi(p); }

        // Build revealed state
        RevealedState rev;
        rev.clickedMask = 0;
        for (int i = 0; i < GRID; ++i) rev.col[i] = CC_UNKNOWN;
        for (const Cell& cell : board) {
            int idx = cell.row * 5 + cell.col;
            if (cell.clicked) {
                rev.clickedMask |= 1u << idx;
                if (cell.color.size() >= 3)
                    rev.col[idx] = parseColor(cell.color.c_str());
            }
        }

        // Opening move: (0,0) — top-left corner.
        // JS: highlightStartingCorners highlights all four corners equally;
        // we pick (0,0) as the canonical first move.
        if (rev.clickedMask == 0) {
            out.row = 0; out.col = 0; return;
        }

        // Compute probabilities
        double probs[GRID] = {};
        if (!computeProbs(rev, nColors, probs)) {
            // No valid boards — fallback to first unclicked cell
            for (int i = 0; i < GRID; ++i) {
                if (!((rev.clickedMask >> i) & 1)) {
                    out.row = i / 5; out.col = i % 5; return;
                }
            }
            out.row = 0; out.col = 0; return;
        }

        int best = scoreAndPick(probs, rev, shipsHit, bluesUsed);
        if (best < 0) {
            for (int i = 0; i < GRID; ++i) {
                if (!((rev.clickedMask >> i) & 1)) {
                    out.row = i / 5; out.col = i % 5; return;
                }
            }
            out.row = 0; out.col = 0; return;
        }
        out.row = best / 5;
        out.col = best % 5;
    }
};

